    private Client getClientBySearch() {
        clientServiceImpl = new ClientServiceImpl(getConn());
        switch (newSelection) {
            case SEARCH_TYPE_INN:
                return client = clientServiceImpl.findByInn(inputText);
            case SEARCH_TYPE_FIO_DATE:
                dateChoise = jDateChooser1.getDate();
                String[] fio = inputText.split(" ");
                if(dateChoise != null){
                client = clientServiceImpl.findByNameAndBirthDay(fio[1], fio[0], fio[2], birthDay);
                return client;
                }else {
                    
                  JOptionPane.showMessageDialog(null, "Введите дату!");
                  return client=null;
                }
            case SEARCH_TYPE_PASSPORT:
                return client = clientServiceImpl.findByPassportNumber(inputText);
            default:
                return client=null;
        }
    }
