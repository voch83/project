    private void jComboBoxChangeSearchTypeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxChangeSearchTypeActionPerformed
        newSelection = (String) jComboBoxChangeSearchType.getSelectedItem();
        jTextFieldSearch.setText("");
        switch (newSelection) {
            case SEARCH_TYPE_FIO_DATE:
                jDateChooser1.setEnabled(true);
                jLabel6.setText("Введите ФИО и ДР");
                break;
            case SEARCH_TYPE_PASSPORT:
                jDateChooser1.setEnabled(false);
                jLabel6.setText("Серия + № паспорта");
                jDateChooser1.setDate(null);
                break;
            default:
                jLabel6.setText("Введите номер ИНН");
                jDateChooser1.setEnabled(false);
                jDateChooser1.setDate(null);
                break;
        }
    }//GEN-LAST:event_jComboBoxChangeSearchTypeActionPerformed

    private void jTextFieldSearchKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldSearchKeyTyped
        char c = evt.getKeyChar();
        switch (newSelection) {
            case SEARCH_TYPE_INN:
                if (!(Character.isDigit(c) || (c == KeyEvent.VK_BACK_SPACE) || c == KeyEvent.VK_DELETE)) {
                    getToolkit().beep();
                    evt.consume();
                }
                break;
            case SEARCH_TYPE_FIO_DATE:
                if (!(Character.isLetter(c) || (c == KeyEvent.VK_SPACE) ||c == KeyEvent.VK_BACK_SPACE)) {
                  getToolkit().beep();
                  evt.consume();
              } break;
        }
    }//GEN-LAST:event_jTextFieldSearchKeyTyped

